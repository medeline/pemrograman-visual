    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
            
            String sql = "insert into data_p (NIK,nama,gender,noHp,alamat,treatment,jumlah,tgl_masuk,tgl_ambil) values(?,?,?,?,?,?,?,?,?)";
            try {
                PreparedStatement pst = koneksi.con.prepareStatement(sql);
                pst.setString(1, nik.getText());
                pst.setString(2, nama.getText());
                pst.setString(3, gender);
                pst.setString(4, noHp.getText());
                pst.setString(5, alamat.getText());
                pst.setString(6, (String) treatment.getSelectedItem());
                pst.setString(7, jumlah.getText());
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date = sdf.format(tgl_msk.getDate());
                pst.setString(8, date);
                SimpleDateFormat sdf2 = new SimpleDateFormat("yyyy-MM-dd");
                String date2 = sdf2.format(tgl_ambil.getDate());
                pst.setString(9, date2);
                pst.execute();
                JOptionPane.showMessageDialog(null,"added data has been successful");
                tampil();
                kosongkan_form();
                
                
            } catch (SQLException ex) {
                Logger.getLogger(Data_Pelanggan.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_addActionPerformed

    private void tabel_dataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_dataMouseClicked
        int baris = tabel_data.rowAtPoint(evt.getPoint());
        String ni = tabel_data.getValueAt(baris,1).toString();
        nik.setText(ni);
        String n = tabel_data.getValueAt(baris,2).toString();
        nama.setText(n);
        String g = tabel_data.getValueAt(baris,3).toString();
        if(g.equals("L")){
            laki.setSelected(true);
            per.setSelected(false);
        }
        else if(g.equals("P")){
            per.setSelected(true);
            laki.setSelected(false);
        }
        String no = tabel_data.getValueAt(baris,4).toString();
        noHp.setText(no);
        String a = tabel_data.getValueAt(baris,5).toString();
        alamat.setText(a);
        String t = tabel_data.getValueAt(baris,6).toString();
        treatment.setSelectedItem(t);
        String j = tabel_data.getValueAt(baris,7).toString();
        jumlah.setText(j);
        

            try {
                DefaultTableModel model = (DefaultTableModel)tabel_data.getModel();
                int s = tabel_data.getSelectedRow();
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String)model.getValueAt(s, 8).toString());
                tgl_msk.setDate(date);
                Date date2 = new SimpleDateFormat("yyyy-MM-dd").parse((String)model.getValueAt(s, 9).toString());
                tgl_ambil.setDate(date2);                
            } catch (ParseException ex) {
                Logger.getLogger(Data_Pelanggan.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_tabel_dataMouseClicked

