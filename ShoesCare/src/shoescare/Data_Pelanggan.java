    public void tampil(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("NIK");
        model.addColumn("Nama");
        model.addColumn("Gender");
        model.addColumn("No. Hp");
        model.addColumn("Alamat");
        model.addColumn("Treatment");
        model.addColumn("jumlah");
        model.addColumn("tgl_masuk");
        model.addColumn("tgl_ambil");
        
        try{
            int no = 1;
            st =(Statement) koneksi.con.createStatement();
            String sql = "SELECT * FROM data_p";
            rs = st.executeQuery(sql);
            
            while(rs.next()){
                model.addRow(new Object[]{no++,rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8),rs.getString(9)});
                
            }
            tabel_data.setModel(model);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"error : "+e.getMessage());
        }
    }
